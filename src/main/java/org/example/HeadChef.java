package org.example;

public class HeadChef {
    private int x;
    private int y;
    private int diameter = 65;

    HeadChef(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() { return x; }

    public int getY() { return y; }

    public int getDiameter() { return diameter; }
}
